class Classifier_General(nn.Module):
    def __init__(self, num_classes=2, num_layer=2, fea_dim=512, prob=0.1, mid_dim=256):
        super(Classifier_General, self).__init__()

        layers = []
        layers.append(nn.Dropout(p=prob))
        layers.append(nn.Conv2d(fea_dim, mid_dim, kernel_size=3, padding=1, bias=False))
        layers.append(nn.BatchNorm2d(mid_dim, affine=True))
        layers.append(nn.ReLU(inplace=True))

        for i in range(num_layer):
            layers.append(nn.Dropout(p=prob))
            layers.append(nn.Conv2d(mid_dim, mid_dim, kernel_size=3, padding=1, bias=False))
            layers.append(nn.BatchNorm2d(mid_dim, affine=True))
            layers.append(nn.ReLU(inplace=True))
        layers.append(nn.Conv2d(mid_dim, num_classes, kernel_size=1))
        self.classifier = nn.Sequential(*layers)

    def forward(self, x):
        x = self.classifier(x)
        return x


